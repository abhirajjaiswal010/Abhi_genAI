# ───────────────────────── Error Handling ─────────────────────────
# Error handling is the mechanism Python uses to prevent programs
# from crashing when unexpected situations occur
# (e.g., invalid input, missing files, or division by zero).
#
# Python handles such situations using *exceptions*.
#
# Common built-in exceptions:
# - ZeroDivisionError     → Division by zero
# - ValueError            → Invalid value/type conversion
# - TypeError             → Operation on incompatible types
# - IndexError            → Index out of range
# - KeyError              → Missing dictionary key
# - FileNotFoundError     → File does not exist
# ──────────────────────────────────────────────────────────────────

order=["masala","ginger"]
# IndexError: list index out of range
print(order[2]) 




